package leavemanager.example.com.leavemanager.node;

public class LeaveInfo {
    private String proposers;
    private String startTime;
    private String endTIme;
    private String siger;
    private String permitTime;
    public String getProposers() {
        return proposers;
    }

    public void setProposers(String proposers) {
        this.proposers = proposers;
    }

    public String getStartTime() {
        return startTime;
    }

    public void setStartTime(String startTime) {
        this.startTime = startTime;
    }

    public String getEndTIme() {
        return endTIme;
    }

    public void setEndTIme(String endTIme) {
        this.endTIme = endTIme;
    }

    public String getSiger() {
        return siger;
    }

    public void setSiger(String siger) {
        this.siger = siger;
    }

    public String getPermitTime() {
        return permitTime;
    }

    public void setPermitTime(String permitTime) {
        this.permitTime = permitTime;
    }



}
